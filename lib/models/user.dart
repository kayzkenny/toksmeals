class User {
  final String uid;

  User({this.uid});
}

class UserData {
  final String address;
  final String email;
  final String firstName;
  final String lastName;
  final String phoneNumber;
  final String zipCode;
  final String uid;

  UserData({
    this.address,
    this.email,
    this.firstName,
    this.lastName,
    this.phoneNumber,
    this.zipCode,
    this.uid,
  });
}
